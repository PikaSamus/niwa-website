	<h1>Introducing Edit-by-Pigeon!</h1>
	<p>For years, editors have been restricted to contributing to their favorite NIWA wikis via the internet, tarnished by poor connections, clunky keyboards, and weary arguments. Through a grand collaboration, several NIWA members have created a brand new service that allows anyone to submit their edits via pigeon (or pigeon-like) post.</p>

	<h1>Steps</h1>

	<p>Ready to submit an edit? Follow these steps:</p>

	<ol>
		<li>Print out a copy of the article you would like to edit.</li>
		<li>Mark the copy with your edits in red ink.</li>
		<li>Request a carrier pigeon from the wiki you are editing.</li>
		<li>In 2 to 30 days, the pigeon should arrive at your home to pick up your modified article.</li>
	</ol>

	<p>Our dedicated pigeons will carry your articles over to the respective wiki's staff team, who will take care to carefully transcribe your edits onto the wiki. Please note that if the article in question has been edited since your submission, your edits will be rejected and you will have to repeat the process from Step 1.</p>

